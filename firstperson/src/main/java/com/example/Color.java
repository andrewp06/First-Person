package com.example;

public class Color {
    float r;
    float g;
    float b;

    public Color(float r, float g, float b){
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public float getB() {
        return b;
    }
    public float getG() {
        return g;
    }
    public float getR() {
        return r;
    }
}
